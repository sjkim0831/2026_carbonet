SELECT 'A0050303', '외부 연계 로그', 'External Logs', '/admin/external/logs', 'list_alt', 'Y', CURRENT_DATETIME, CURRENT_DATETIME FROM db_root
WHERE NOT EXISTS (
    SELECT 1
    FROM COMTNMENUINFO
    WHERE MENU_CODE = 'A0050303'
);

UPDATE COMTNMENUINFO
SET MENU_NM = '외부 연계 로그',
    MENU_NM_EN = 'External Logs',
    MENU_URL = '/admin/external/logs',
    MENU_ICON = 'list_alt',
    USE_AT = 'Y',
    LAST_UPDT_PNTTM = CURRENT_DATETIME
WHERE MENU_CODE = 'A0050303';

INSERT INTO COMTNMENUORDER (
    MENU_CODE,
    SORT_ORDR,
    FRST_REGIST_PNTTM,
    LAST_UPDT_PNTTM
)
SELECT 'A0050303', 3, CURRENT_DATETIME, CURRENT_DATETIME FROM db_root
WHERE NOT EXISTS (
    SELECT 1
    FROM COMTNMENUORDER
    WHERE MENU_CODE = 'A0050303'
);

UPDATE COMTNMENUORDER
SET SORT_ORDR = 3,
    LAST_UPDT_PNTTM = CURRENT_DATETIME
WHERE MENU_CODE = 'A0050303';

INSERT INTO COMTNMENUFUNCTIONINFO (
