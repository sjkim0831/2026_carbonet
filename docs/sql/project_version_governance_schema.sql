-- Project version governance draft
-- Generated on 2026-04-08 for artifact registry and adapter-safe common upgrade flow

CREATE TABLE ARTIFACT_VERSION_REGISTRY (
    ARTIFACT_VERSION_ID        VARCHAR(80) PRIMARY KEY,
    ARTIFACT_ID                VARCHAR(120) NOT NULL,
    ARTIFACT_TYPE              VARCHAR(40) NOT NULL,
    ARTIFACT_FAMILY            VARCHAR(40) NOT NULL,
    ARTIFACT_VERSION           VARCHAR(40) NOT NULL,
    GROUP_LINE                 VARCHAR(60),
    STORAGE_KEY                VARCHAR(255) NOT NULL,
    CHECKSUM_SHA256            VARCHAR(64) NOT NULL,
    COMMON_CORE_VERSION        VARCHAR(40),
    ADAPTER_CONTRACT_VERSION   VARCHAR(40),
    API_CONTRACT_VERSION       VARCHAR(40),
    MANIFEST_CONTRACT_VERSION  VARCHAR(40),
    CAPABILITY_CATALOG_VERSION VARCHAR(40),
    COMPATIBILITY_STATUS       VARCHAR(30) NOT NULL,
    PUBLISHED_YN               CHAR(1) DEFAULT 'N' NOT NULL,
    BUILT_AT                   DATETIME,
    PUBLISHED_AT               DATETIME,
    PUBLISHED_BY               VARCHAR(80)
);

CREATE UNIQUE INDEX UX_ARTIFACT_VERSION_REGISTRY_KEY
    ON ARTIFACT_VERSION_REGISTRY (ARTIFACT_ID, ARTIFACT_VERSION);

CREATE TABLE PROJECT_ARTIFACT_INSTALL (
    PROJECT_ARTIFACT_INSTALL_ID VARCHAR(80) PRIMARY KEY,
    PROJECT_ID                  VARCHAR(80) NOT NULL,
    ARTIFACT_VERSION_ID         VARCHAR(80) NOT NULL,
    INSTALL_SCOPE               VARCHAR(40) NOT NULL,
    RELEASE_UNIT_ID             VARCHAR(120),
    ACTIVE_YN                   CHAR(1) DEFAULT 'Y' NOT NULL,
    ROLLBACK_TARGET_VERSION     VARCHAR(40),
    INSTALLED_AT                DATETIME,
    INSTALLED_BY                VARCHAR(80)
);

CREATE INDEX IX_PROJECT_ARTIFACT_INSTALL_PROJECT
    ON PROJECT_ARTIFACT_INSTALL (PROJECT_ID, ACTIVE_YN);

CREATE TABLE ADAPTER_CHANGE_LOG (
    ADAPTER_CHANGE_ID          VARCHAR(80) PRIMARY KEY,
    PROJECT_ID                 VARCHAR(80) NOT NULL,
    ADAPTER_ARTIFACT_VERSION   VARCHAR(40) NOT NULL,
    ADAPTER_CONTRACT_VERSION   VARCHAR(40) NOT NULL,
    CHANGED_PORT_SET_JSON      CLOB,
    CHANGED_DTO_SET_JSON       CLOB,
    MAPPING_IMPACT_SUMMARY     VARCHAR(1000),
    COMPATIBILITY_CLASS        VARCHAR(40) NOT NULL,
    MIGRATION_REQUIRED_YN      CHAR(1) DEFAULT 'N' NOT NULL,
    RELATED_RELEASE_UNIT_ID    VARCHAR(120),
    RECORDED_AT                DATETIME NOT NULL,
    RECORDED_BY                VARCHAR(80)
);

CREATE INDEX IX_ADAPTER_CHANGE_LOG_PROJECT
    ON ADAPTER_CHANGE_LOG (PROJECT_ID, RECORDED_AT);

CREATE TABLE RELEASE_UNIT_REGISTRY (
    RELEASE_UNIT_ID            VARCHAR(120) PRIMARY KEY,
    PROJECT_ID                 VARCHAR(80) NOT NULL,
    RUNTIME_PACKAGE_ID         VARCHAR(120) NOT NULL,
    PROJECT_RUNTIME_VERSION    VARCHAR(40) NOT NULL,
    ADAPTER_ARTIFACT_VERSION   VARCHAR(40),
    ADAPTER_CONTRACT_VERSION   VARCHAR(40),
    COMMON_ARTIFACT_SET_JSON   CLOB,
    PACKAGE_VERSION_SET_JSON   CLOB,
    ROLLBACK_TARGET_RELEASE_ID VARCHAR(120),
    BUILT_AT                   DATETIME,
    APPROVED_AT                DATETIME,
    APPROVED_BY                VARCHAR(80)
);

CREATE INDEX IX_RELEASE_UNIT_REGISTRY_PROJECT
    ON RELEASE_UNIT_REGISTRY (PROJECT_ID, BUILT_AT);

CREATE TABLE SERVER_DEPLOYMENT_STATE (
    SERVER_DEPLOYMENT_ID       VARCHAR(80) PRIMARY KEY,
    SERVER_ID                  VARCHAR(80) NOT NULL,
    SERVER_ROLE                VARCHAR(30),
    PROJECT_ID                 VARCHAR(80) NOT NULL,
    RELEASE_UNIT_ID            VARCHAR(120) NOT NULL,
    DEPLOY_TRACE_ID            VARCHAR(120) NOT NULL,
    HEALTH_STATUS              VARCHAR(30),
    DEPLOYED_AT                DATETIME,
    DEPLOYED_BY                VARCHAR(80)
);

CREATE INDEX IX_SERVER_DEPLOYMENT_STATE_SERVER
    ON SERVER_DEPLOYMENT_STATE (SERVER_ID, PROJECT_ID, DEPLOYED_AT);

CREATE TABLE PROJECT_DEPLOYMENT_HISTORY (
    DEPLOYMENT_HISTORY_ID VARCHAR(80) PRIMARY KEY,
    PROJECT_ID            VARCHAR(80) NOT NULL,
    RELEASE_VERSION       VARCHAR(80) NOT NULL,
    RELEASE_TITLE         VARCHAR(200),
    RELEASE_CONTENT       VARCHAR(4000),
    STATUS                VARCHAR(30) NOT NULL,
    SERVER_TARGET         VARCHAR(200),
    RELEASE_UNIT_ID       VARCHAR(120),
    DEPLOY_TRACE_ID       VARCHAR(120),
    GIT_COMMIT_SHA        VARCHAR(80),
    RESULT_MESSAGE        VARCHAR(1000),
    STARTED_AT            DATETIME NOT NULL,
    FINISHED_AT           DATETIME,
    UPDATED_AT            DATETIME NOT NULL,
    CREATED_BY            VARCHAR(80)
);

CREATE INDEX IX_PROJECT_DEPLOYMENT_HISTORY_PROJECT
    ON PROJECT_DEPLOYMENT_HISTORY (PROJECT_ID, STARTED_AT);
