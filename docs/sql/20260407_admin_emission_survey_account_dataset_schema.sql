ALTER TABLE ADMIN_EMISSION_SURVEY_CASE
    ADD COLUMN OWNER_ACTOR_ID VARCHAR(60);

ALTER TABLE ADMIN_EMISSION_SURVEY_CASE
    ADD COLUMN DATASET_ID VARCHAR(40);

ALTER TABLE ADMIN_EMISSION_SURVEY_CASE
    ADD COLUMN DATASET_NAME VARCHAR(200);

UPDATE ADMIN_EMISSION_SURVEY_CASE
SET OWNER_ACTOR_ID = COALESCE(OWNER_ACTOR_ID, LAST_SAVED_BY, ''),
    DATASET_ID = COALESCE(DATASET_ID, ''),
    DATASET_NAME = COALESCE(DATASET_NAME, '');

DROP INDEX IDX_ADMIN_EMISSION_SURVEY_CASE_01 ON ADMIN_EMISSION_SURVEY_CASE;

CREATE UNIQUE INDEX IDX_ADMIN_EMISSION_SURVEY_CASE_01
    ON ADMIN_EMISSION_SURVEY_CASE (OWNER_ACTOR_ID, SECTION_CODE, CASE_CODE, LCI_MAJOR_CODE, LCI_MIDDLE_CODE, LCI_SMALL_CODE);

CREATE INDEX IDX_ADMIN_EMISSION_SURVEY_CASE_04
    ON ADMIN_EMISSION_SURVEY_CASE (OWNER_ACTOR_ID, DATASET_ID, LAST_SAVED_AT);

CREATE TABLE ADMIN_EMISSION_SURVEY_UPLOAD_LOG (
    LOG_ID               VARCHAR(40)   NOT NULL,
    DATASET_ID           VARCHAR(40),
    OWNER_ACTOR_ID       VARCHAR(60)   NOT NULL,
    SOURCE_FILE_NM       VARCHAR(300),
    LCI_MAJOR_CODE       VARCHAR(20),
    LCI_MAJOR_LABEL      VARCHAR(200),
    LCI_MIDDLE_CODE      VARCHAR(20),
    LCI_MIDDLE_LABEL     VARCHAR(200),
    LCI_SMALL_CODE       VARCHAR(20),
    LCI_SMALL_LABEL      VARCHAR(200),
    RESULT_STATUS        VARCHAR(20)   NOT NULL,
    MESSAGE              VARCHAR(1000),
    SECTION_COUNT        INT           NOT NULL DEFAULT 0,
    ROW_COUNT            INT           NOT NULL DEFAULT 0,
    STORAGE_MODE         VARCHAR(30),
    SECTION_RESULT_JSON  CLOB,
    UPLOADED_AT          DATETIME      NOT NULL DEFAULT CURRENT_DATETIME,
    PRIMARY KEY (LOG_ID)
);

CREATE INDEX IDX_ADMIN_EMISSION_SURVEY_UPLOAD_LOG_01
    ON ADMIN_EMISSION_SURVEY_UPLOAD_LOG (OWNER_ACTOR_ID, UPLOADED_AT);

CREATE INDEX IDX_ADMIN_EMISSION_SURVEY_UPLOAD_LOG_02
    ON ADMIN_EMISSION_SURVEY_UPLOAD_LOG (OWNER_ACTOR_ID, LCI_MAJOR_CODE, LCI_MIDDLE_CODE, LCI_SMALL_CODE, RESULT_STATUS);
